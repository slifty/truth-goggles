			<h1>Gun Control: The Great Debate</h1>
			<p>In the nine months since a gunman killed 20 schoolchildren in Newtown, Conn., the conversation around American gun culture has been re-launched into the center of national attention. At the root of the debate are core disagreements around both the impact of gun violence, and the best way to address that impact.</p>
			<p>In many cases, the American public's understanding of the issue comes down to a deciphering of claims made by both sides of the table regarding the nature of American gun violence.</p>
			<p>Supporters of gun control argue that the numbers reflect a significant and unnecessary loss of human life. For example Mark Shields, an advocate for gun control, pointed out that since 1968 more Americans have died from gunfire than died in all the wars of the country's history.</p>
			<p>Gun rights advocates do not associate the right to bear arms with violent crime. Faced with Mark's argument they would be quick to point out that the numbers indicate a safer future, and that over the past twenty years, the number of homicides committed with a firearm in the United States has decreased by nearly 40 percent.
			<p>As both sides of the public relations war cite facts in an attempt to sway hearts and minds, only one thing is clear: the nation is divided on this issue.</p>
			<p>Gun control supporters faced some success following the Newtown shooting in the form of sweeping new laws in a handful of Democratic-led states, including Maryland and New York, as well as in politically mixed Colorado. But more than two dozen states, most of them controlled by Republicans, moved in the opposite direction, expanding the rights of gun owners.</p>
			<p>These laws generally come in three flavors. The first affects what types of weapons can be sold, the second attempts to regulate the nature of the weapons (for instance, clip sizes), and the third focuses on keeping guns out the hands of criminals and the mentally unstable through stronger background checks.</p>
			<p>The rationale behind each category are different, but gun rights lawmakers are skeptical that new legislation will do anything to improve the numbers. Some feel that gun laws are linked to areas with high crime, although contrary to arguments made by Republican Senator Ted Cruz, the jurisdictions with the strictest gun control laws do not always have the highest crime rates or the highest murder rates.</p>
			<p>Meanwhile, Democrats are convinced that the best way to stop gun violence is to change the rules around gun ownership. They see members of their leadership, like Joe Biden, making claims about the number of guns purchased without background checks but critics point out that Biden's data around gun purchases are out of date.</p>
			<p>As America's relationship with guns continues to evolve, the end of the debate is nowhere in sight. Expect to hear arguments, statistics, and information about new legislation from both sides of the table for quite some time.</p>
